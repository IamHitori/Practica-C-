#include<iostream>
using namespace std;

int main(){
    //Tipos de datos basicos

    //Enteros (1)
    int numero_entero = 1;
    //Flotante (1.1)
    float numero_decimal = 1.1;
    //Double (Es lo mismo que el flotante pero en este puedes guardar datos mayores)
    double numero_decimal_mayor = 1.12334445667789;
    //char es la variable para guardar caracteres
    char caracter = 'a';

    cout<<numero_entero<<endl; // Lo que va despues de cout<<"nombre de la variables" es el nombre de la variable
    cout<<numero_decimal<<endl;
    cout<<numero_decimal_mayor<<endl;
    cout<<caracter<<endl;
    return 0;
}